fun AssetDetailModel.withRemoteDetails(remote: AssetDetailDto): AssetDetailModel = copy(
    objectKey = remote.objectKey,
    takenAtSource = remote.takenAtSource,
    timezoneOffsetMinutes = remote.timezoneOffsetMinutes,
    software = remote.software,
    isHidden = remote.isHidden,
    trashedAt = remote.trashedAt,
    createdAt = remote.createdAt,
)

fun AssetDetailModel.withLocalReplica(local: AssetDetailModel): AssetDetailModel = local.copy(
    objectKey = objectKey,
    takenAtSource = takenAtSource,
    timezoneOffsetMinutes = timezoneOffsetMinutes,
    software = software,
    isHidden = isHidden,
    trashedAt = trashedAt,
    createdAt = createdAt,
)
