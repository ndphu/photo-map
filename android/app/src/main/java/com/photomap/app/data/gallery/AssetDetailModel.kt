package com.photomap.app.data.gallery

import com.photomap.app.data.local.RemoteAssetEntity
import com.photomap.app.data.network.AssetDetailDto

data class AssetDetailModel(
    val id: String,
    val mediaType: String,
    val mimeType: String,
    val objectKey: String? = null,
    val thumbnailKey: String? = null,
    val previewKey: String? = null,
    val posterFrameKey: String? = null,
    val thumbnailUrl: String? = null,
    val previewUrl: String? = null,
    val posterFrameUrl: String? = null,
    val originalFilename: String? = null,
    val fileSizeBytes: Long? = null,
    val takenAt: String? = null,
    val width: Int? = null,
    val height: Int? = null,
    val durationMs: Long? = null,
    val latitude: Double? = null,
    val longitude: Double? = null,
    val city: String? = null,
    val isFavorite: Boolean = false,
    val isArchived: Boolean = false,
    val isTrashed: Boolean = false,
    val checksumSha256: String? = null,
    val takenAtSource: String? = null,
    val timezoneOffsetMinutes: Int? = null,
    val orientation: Int? = null,
    val country: String? = null,
    val region: String? = null,
    val placeName: String? = null,
    val cameraMake: String? = null,
    val cameraModel: String? = null,
    val software: String? = null,
    val isHidden: Boolean = false,
    val trashedAt: String? = null,
    val uploadedAt: String? = null,
    val createdAt: String? = null,
    val updatedAt: String? = null,
)

